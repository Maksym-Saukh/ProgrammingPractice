#include <stdio.h>

// Функція для підрахунку кількості рівних дільників числа n
int countDivisors(int n) {
    int count = 0;

    // Перебираємо всі числа від 1 до n
    for (int i = 1; i <= n; i++) {
        // Перевіряємо, чи співпадають частка та залишок від ділення n на i
        if (n % i == n / i) {
            count++; // Якщо умова виконується, збільшуємо лічильник
        }
    }

    return count; // Повертаємо кількість рівних дільників
}

int main() {
    int n;
    printf("Введіть натуральне число n: ");
    scanf("%d", &n);

    // Перевіряємо, чи введене число n відповідає обмеженням 1 < n < 150
    if (n <= 1 || n >= 150) {
        printf("Введене число n не відповідає обмеженням.\n");
        return 0;
    }

    int result = countDivisors(n); // Обчислюємо кількість рівних дільників
    printf("Кількість рівних дільників числа %d: %d\n", n, result);

    return 0;
}
